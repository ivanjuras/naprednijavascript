
/*

  Operator je specijalna funkcija koje se sintaktički piše drugačije od ostalih funkcija.

  U standardnim slučajevima, operatori primaju dva parametra i vraćaju jednu vrijednost.

*/

// ovo je slično kao da smo napisali function +( a, b ) { return a + b }
var a = 3 + 4;

console.log( a );

// ovo je infiksna notacija, umjesto da smo napisali +( 3, 4 )
3 + 4

// vraća boolean vrijednost
var a = 4 > 3;

console.log( a );